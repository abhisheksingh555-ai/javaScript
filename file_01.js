"use strict"
// =======================================
// Introduction to Variables in JavaScript
// =======================================

// A Variable is a container used to store data values.

// Why do we use variables?
// 1. To store information
// 2. To reuse that information later
// 3. To update or change the stored value when needed


// =======================================
// Declaring a Variable
// =======================================


// Example:
var firstName = "Abhishek";


// =======================================
// Using a Variable
// =======================================

// We can access the stored value using the variable name

console.log(firstName); 
// Output: Abhishek


// =======================================
// Changing (Updating) a Variable Value
// =======================================

// Variables declared with 'var' can be reassigned

firstName = "Abhishek Singh";

console.log(firstName);
// Output: Abhishek Singh


// =======================================
// Key Points
// =======================================

// 1. 'var' is used to declare variables (older way).
// 2. Variable names should be meaningful.
// 3. JavaScript is case-sensitive.
//    Example: firstName and firstname are different.