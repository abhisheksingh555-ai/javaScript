"use strict"
// ==================================
// For Loop in JavaScript
// ==================================

// A for loop is used when we know how many times
// we want to run a block of code.


// Syntax

// for(initialization; condition; update){
//     code to execute
// }


// ==================================
// Example
// ==================================

for(let i = 1; i <= 5; i++){
    console.log(i);
}

// Output
// 1
// 2
// 3
// 4
// 5


